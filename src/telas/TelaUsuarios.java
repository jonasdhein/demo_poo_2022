    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try{
            boolean validou = validarCampos();
            //validar os dados -> verificar se estao preenchidos
            if(validou){
                
                Usuario objeto = preencherObjeto();//vai estar preenchido nome, login e senha
               
                if(objeto != null){
                    
                    controller = new UsuarioController();
                    boolean retorno = false;
                    
                    if(objeto.getId() == 0){
                        retorno = controller.incluir(objeto);
                    }else{
                        retorno = controller.alterar(objeto);
                    }
                    
                    if(retorno){
                        Globais.exibirMensagem("Sucesso!");
                    }else{
                        Globais.exibirMensagem("Erro ao salvar usuário!");
                    }
                }
            }
            
        }catch(Exception ex){
            Globais.exibirMensagem("Erro ao salvar!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
        try{
            
            txtNome.setText("");
            txtUsuario.setText("");

            //txtId.setEnabled(true);
            txtSenha.setText("");
            txtSenha.setEnabled(true);                    
            txtConfirmarSenha.setEnabled(true);
            txtConfirmarSenha.setText("");
            
        }catch(Exception ex){
            System.out.println("ERRO: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnLimparActionPerformed

    private Usuario preencherObjeto(){
        try{
            objeto = new Usuario();
        
            objeto.setNome(txtNome.getText());
            objeto.setLogin(txtUsuario.getText());
            objeto.setSenha(txtSenha.getText());

            return objeto;
            
        }catch(Exception ex){
            Globais.exibirMensagem("Erro ao preencher o objeto");
            return null;
        }
    }
    
    private boolean validarCampos(){
        
        if(txtNome.getText().equals("")){
            Globais.exibirMensagem("Informe corretamente o nome");
            return false;
        }else if(txtUsuario.getText().equals("")){
            Globais.exibirMensagem("Informe corretamente o usuário");
            return false;
        }else if(txtSenha.getText().equals("") || !txtSenha.getText().equals(txtConfirmarSenha.getText())){
            Globais.exibirMensagem("Informe corretamente a senha");
            return false;
        }
        
        return true;
    }
    
